namespace CS_Color
{
    struct MyColor
    {
        public byte a = 255;
        public byte r = 255;
        public byte g = 255;
        public byte b = 255;
        public MyColor(byte aa = 0, byte ar = 0, byte ag = 0, byte ab = 0)
        {
            a = aa;
            r = ar;
            g = ag;
            b = ab;
        }
    }
}